# 平均値を計算する関数
def calculate_mean(data):
    return sum(data) / len(data)

# 標準偏差を計算する関数
def calculate_std(data, mean):
    return (sum((x - mean) ** 2 for x in data) / len(data)) ** 0.5

# 地域ごとの統計値を計算
regions = {
    '全国': range(len(pref)),
    '本州': [i for i, p in enumerate(pref) if p in honshu],
    '九州': [i for i, p in enumerate(pref) if p in kyushu],
    '四国': [i for i, p in enumerate(pref) if p in shikoku],
}

for region_name, indices in regions.items():
    print(f"地域: {region_name}")
    for label, data in zip(['男性平均身長', '男性平均体重', '女性平均身長', '女性平均体重'],
                           [m_height, m_weight, f_height, f_weight]):
        # 該当する地域のデータを抽出
        region_data = [data[i] for i in indices]
        mean = calculate_mean(region_data)
        std = calculate_std(region_data, mean)
        print(f"  {label}: 平均 = {mean:.2f}, 標準偏差 = {std:.2f}")
    print()
